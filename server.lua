local QBCore = exports['qb-core']:GetCoreObject()

-- Server Events --
RegisterServerEvent("power-oilrig:GivOlieRig1")
AddEventHandler("power-oilrig:GivOlieRig1", function(amount)
    local src = source
    local Player = QBCore.Functions.GetPlayer(src)
    local a = 0
    repeat
        Player.Functions.AddItem("raaolie", 1)
        TriggerClientEvent('inventory:client:ItemBox', source, QBCore.Shared.Items["raaolie"], "add")
        a = a + 1
    until( a == amount )   
end)

